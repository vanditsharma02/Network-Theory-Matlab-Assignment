P = [];
A = [L1 L2 L3 C1 C2 C3];
q = sqrt(-1);

for j= 1:1000
	for i= 1:6
		B(i) = A(i)+(-0.1+0.2*rand)*A(i);
    end
    
    n6 = B(1)*B(2)*B(3)*B(4)*B(5)*B(6);
    n4 = (B(5)*B(2)*B(1)*B(4))+(B(6)*B(3)*B(1)*B(4))+(B(2)*B(6)*B(1)*B(4))+(B(6)*B(3)*B(5)*B(1))+(B(2)*B(6)*B(5)*B(1))+(B(5)*B(6)*B(3)*B(2));
    n2 = (B(1)*B(4))+(B(5)*B(1))+(B(5)*B(2))+(B(3)*B(6))+(B(2)*B(6));
    n0 = 1;
    d5 = (B(5)*B(6)*B(3)*B(2)*B(4));
    d3 = (B(5)*B(2)*B(4))+(B(6)*B(3)*B(4))+(B(2)*B(6)*B(4))+(B(6)*B(3)*B(5))+(B(2)*B(6)*B(5));
    d1 = B(4)+B(5);
    
    numz = [n6 0 n4 0 n2 0 n0];
	denz = [d5 0 d3 0 d1 0];
	Z = tf(numz,denz);

	P = cat(1,P,abs(pole(Z)/q));
end

